public class SwitchCasePlanoOperadora {
    public static void main(String[] args) {
        String plano = "M";
        switch (plano) {
            case "T":{
                System.out.println("5Gb YouTube.");
                
            }
            case "M":{
                System.out.println("Whats e Integram grátis.");
            }
            case "B":{
                System.out.println("100 minutos de ligação.");
            }
                
        }
    }
}
